    private void findButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        String properties = propertiesFindComboBox.getSelectedItem().toString();
        if (!findTextField.getText().equals("")) {
            defaultTableModel.setRowCount(0);
            if ("id".equals(properties)) {
                setTableData((List<Book>) bookService.findById(Integer.parseInt(findTextField.getText())));
            }
            if ("author".equals(properties)) {
                setTableData(bookService.findByAuthor(findTextField.getText()));
            }
            defaultTableModel.fireTableDataChanged();
        }

    }//GEN-LAST:event_findButtonActionPerformed

