    private void setTableData(List<Book> books) {
        for (Book book : books) {
            defaultTableModel.addRow(new Object[]{
                book.getId(),
                book.getProducer(),
                book.getReleaseNumber(),
                book.getAuthor(),
                book.getNumberOfPages()});
        }

    }
